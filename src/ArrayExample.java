public class ArrayExample {
    public static void main(String[] args) {
        int []  numbers = {10, 20, 30, 40, 50};
        System.out.println(numbers[2]);

        for(int i = 0; i < numbers.length; i++){
            System.out.println("Element is " + i + " is " + numbers[i]);
        }

    }
}
